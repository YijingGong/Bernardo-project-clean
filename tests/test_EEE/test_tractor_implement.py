@pytest.mark.parametrize(
    "operation_event, operation_type, crop_type, tractor_size, tillage_implement, application_depth",
    [
        (
            FieldOperationEvent.HARVEST,
            OperationType.MOWING,
            "alfalfa_hay",
            TractorSize.SMALL,
            TillageImplement.DISK_HARROW,
            10.0,
        ),
        (
            FieldOperationEvent.MANURE_APPLICATION,
            OperationType.LIQUID_MANURE_APPLICATION_SURFACE,
            "corn_silage",
            TractorSize.MEDIUM,
            TillageImplement.SUBSOILER,
            0.0,
        ),
        (
            FieldOperationEvent.FERTILIZER_APPLICATION,
            OperationType.FERTILIZER_APPLICATION_SURFACE,
            "tall_fescue_hay",
            TractorSize.LARGE,
            TillageImplement.CULTIVATOR,
            10.0,
        ),
    ],
)
def test_init(
    operation_event: FieldOperationEvent,
    operation_type: OperationType,
    crop_type: str | None,
    tractor_size: TractorSize,
    tillage_implement: TillageImplement | None,
    application_depth: float | None,
    EEE_constants: list[dict[str, Any]],
    mocker: MockerFixture,
) -> None:
    """Tests the initialization of TractorImplement with various parameters."""
    im = InputManager()
    mock_get_data = mocker.patch.object(im, "get_data", return_value=deepcopy(EEE_constants))
    mock_determine_implement_parameters = mocker.patch(
        "RUFAS.EEE.tractor_implement.TractorImplement.determine_implement_parameters"
    )

    tractor_implement = TractorImplement(
        operation_event=operation_event,
        operation_type=operation_type,
        crop_type=crop_type,
        tractor_size=tractor_size,
        tillage_implement=tillage_implement,
        application_depth=application_depth,
    )

    assert tractor_implement.operation_event == operation_event
    assert tractor_implement.operation_type == operation_type
    assert tractor_implement.crop_type == crop_type
    assert tractor_implement.tractor_size == tractor_size
    assert tractor_implement.tillage_implement == tillage_implement
    assert tractor_implement.field_speed_km_per_hr == 10.0
    assert tractor_implement.field_efficiency == 0.9

    mock_get_data.assert_called_once_with("EEE_constants.constants")
    mock_determine_implement_parameters.assert_called_once_with(application_depth)


